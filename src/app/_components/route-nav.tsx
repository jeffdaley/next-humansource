export default function RouteNav() {
  const navLinks: NavLink[] = [
    {
      href: "/",
      text: "Home",
      icon: HomeIcon(),
    },
    {
      href: "/employees",
      text: "Employees",
      icon: PeopleIcon(),
    },
    {
      href: "/company",
      text: "Company",
      icon: CompanyIcon(),
    },
    {
      href: "/benefits",
      text: "Benefits",
      icon: BenefitsIcon(),
    },
  ];

  const pathname = usePathname();

  const linkIsActive = (href: string) => {
    if (href === "/") {
      return pathname === "/";
    } else {
      return pathname.startsWith(href);
    }
  };

  return (
    <nav className="sticky top-0 flex h-screen w-32 shrink-0 flex-col border-r border-r-neutral-800 px-1.5 text-xs">
      <div className="flex h-full flex-col justify-start pt-1.5">
        {navLinks.map((link) => (
          <Link
            key={link.href}
            href={link.href}
            className="grid place-items-center gap-2 px-3 py-3.5"
          >
            <div
              className={
                "grid h-14 w-14 place-items-center rounded-full border-2 border-neutral-700 " +
                (linkIsActive(link.href) && "bg-neutral-700")
              }
            >
              {link.icon}
            </div>
            {link.text}
          </Link>
        ))}
      </div>
    </nav>
  );
}
